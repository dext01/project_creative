MISTRAL_API_URL = "https://api.mistral.ai/v1/chat/completions"


def _extract_json_from_content(content: str) -> Dict[str, Any]:
    """
    –ü—ã—Ç–∞–µ—Ç—Å—è –∞–∫–∫—É—Ä–∞—Ç–Ω–æ –≤—ã—Ç–∞—â–∏—Ç—å JSON –∏–∑ –ø—Ä–æ–∏–∑–≤–æ–ª—å–Ω–æ–≥–æ —Ç–µ–∫—Å—Ç–∞ LLM.
    1) —Ä–µ–∂–µ–º –ø–æ ```json ... ``` –µ—Å–ª–∏ –µ—Å—Ç—å
    2) –µ—Å–ª–∏ –Ω–µ—Ç ‚Äî –±–µ—Ä–µ–º –ø–æ–¥—Å—Ç—Ä–æ–∫—É –æ—Ç –ø–µ—Ä–≤–æ–π '{' –¥–æ –ø–æ—Å–ª–µ–¥–Ω–µ–π '}'
    3) –ø–∞—Ä—Å–∏–º json.loads
    """
    if not isinstance(content, str):
        raise ValueError(f"–û–∂–∏–¥–∞–ª–∞—Å—å —Å—Ç—Ä–æ–∫–∞ —Å JSON, –Ω–æ –ø—Ä–∏—à–ª–æ: {type(content)}")

    # 1. –ü–æ–ø—Ä–æ–±—É–µ–º –Ω–∞–π—Ç–∏ –±–ª–æ–∫ ```json ... ```
    code_block = re.search(r"```json(.*?)```", content, flags=re.DOTALL | re.IGNORECASE)
    if code_block:
        candidate = code_block.group(1).strip()
        return json.loads(candidate)

    # 2. –ï—Å–ª–∏ –Ω–µ—Ç code-block, –±–µ—Ä–µ–º –æ—Ç –ø–µ—Ä–≤–æ–π { –¥–æ –ø–æ—Å–ª–µ–¥–Ω–µ–π }
    start = content.find("{")
    end = content.rfind("}")
    if start != -1 and end != -1 and end > start:
        candidate = content[start : end + 1].strip()
        return json.loads(candidate)

    # 3. –ü–æ—Å–ª–µ–¥–Ω—è—è –ø–æ–ø—ã—Ç–∫–∞ ‚Äî –º–æ–∂–µ—Ç, —ç—Ç–æ —É–∂–µ —á–∏—Å—Ç—ã–π JSON
    return json.loads(content)


class MistralClient:
    """
    –ö–ª–∏–µ–Ω—Ç –¥–ª—è Mistral API.
    –û–∂–∏–¥–∞–µ—Ç –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é –æ–∫—Ä—É–∂–µ–Ω–∏—è MISTRAL_API_KEY.
    """

    def __init__(self, model: str = "mistral-small-latest"):
        api_key = os.getenv("MISTRAL_API_KEY")
        if not api_key:
            raise ValueError("MISTRAL_API_KEY –Ω–µ –∑–∞–¥–∞–Ω –≤ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö –æ–∫—Ä—É–∂–µ–Ω–∏—è!")
        self.api_key = api_key
        self.model = model

    def generate_variants(self, payload: Dict[str, Any]) -> List[AdVariant]:
        body = {
            "model": self.model,
            "messages": [
                {"role": "system", "content": SYSTEM_PROMPT},
                {"role": "user", "content": json.dumps(payload, ensure_ascii=False)},
            ],
            "temperature": 0.85,
        }

        headers = {
            "Authorization": f"Bearer {self.api_key}",
            "Content-Type": "application/json",
        }

        resp = httpx.post(MISTRAL_API_URL, headers=headers, json=body, timeout=40.0)
        resp.raise_for_status()
        data = resp.json()

        content = data["choices"][0]["message"]["content"]

        # --- –≤–æ—Ç —Ç—É—Ç –±—ã–ª –∫—Ä–∞—à, —Ç–µ–ø–µ—Ä—å –∞–∫–∫—É—Ä–∞—Ç–Ω–æ –≤—ã—Ç–∞—Å–∫–∏–≤–∞–µ–º JSON ---
        try:
            parsed = _extract_json_from_content(content)
        except Exception as e:
            # —á—Ç–æ–±—ã –ª–µ–≥—á–µ –æ—Ç–ª–∞–∂–∏–≤–∞—Ç—å, –≤—ã–∫–∏–¥—ã–≤–∞–µ–º –ø–æ–Ω—è—Ç–Ω—É—é –æ—à–∏–±–∫—É
            raise ValueError(
                f"–ù–µ —É–¥–∞–ª–æ—Å—å —Ä–∞—Å–ø–∞—Ä—Å–∏—Ç—å JSON –∏–∑ –æ—Ç–≤–µ—Ç–∞ Mistral. "
                f"–°—ã—Ä–æ–π –∫–æ–Ω—Ç–µ–Ω—Ç:\n{content[:500]}\n–û—à–∏–±–∫–∞: {e}"
            ) from e

        variants_raw = parsed.get("variants", [])
        variants: List[AdVariant] = []
        for v in variants_raw:
            variants.append(
                AdVariant(
                    channel=v.get("channel", payload.get("channel", "")),
                    headline=v.get("headline", ""),
                    text=v.get("text", ""),
                    cta=v.get("cta", ""),
                    notes=v.get("notes", ""),
                )
            )
        return variants


